-- Tests Advanced Peripherals ME Bridge detection and basic item reads.

local DETECTION_METHODS = {
  "getItems",
  "isOnline",
  "getStoredEnergy",
  "getUsedItemStorage",
  "getAvailableItemStorage"
}

local function typeToString(value)
  if type(value) == "table" then return table.concat(value, ",") end
  return tostring(value or "unknown")
end

local function hasMethod(object, methodName)
  return object and type(object[methodName]) == "function"
end

local function knownMethods(object)
  local found = {}

  for _, methodName in ipairs(DETECTION_METHODS) do
    if hasMethod(object, methodName) then
      table.insert(found, methodName)
    end
  end

  return found
end

local function looksLikeBridge(name)
  local wrapped = peripheral.wrap(name)
  if not wrapped then return false, {} end

  local found = knownMethods(wrapped)
  if #found > 0 then return true, found end

  local kind = string.lower(typeToString(peripheral.getType(name)))
  if string.find(kind, "mebridge", 1, true) or string.find(kind, "me_bridge", 1, true) then
    return true, found
  end

  return false, found
end

local function safeCall(object, methodName, ...)
  if not object or type(object[methodName]) ~= "function" then
    return false, "missing method: " .. tostring(methodName)
  end

  local args = { ... }
  local ok, result = pcall(function()
    return object[methodName](table.unpack(args))
  end)

  if ok then return true, result end
  return false, tostring(result)
end

local function getItems(bridge)
  local ok, result = safeCall(bridge, "getItems")
  if ok and type(result) == "table" then return true, result end

  ok, result = safeCall(bridge, "getItems", {})
  if ok and type(result) == "table" then return true, result end

  return false, result
end

local function itemCount(item)
  if type(item) ~= "table" then return 0 end
  return tonumber(item.amount or item.count or item.size or item.qty or 0) or 0
end

local function itemName(item)
  if type(item) ~= "table" then return tostring(item) end
  return tostring(item.displayName or item.display_name or item.name or item.id or item.fingerprint or "unknown")
end

