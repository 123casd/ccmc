local ADAPTER_MANAGER_LUA = [=====[
-- /sgnet/lib/adapter_manager.lua
-- Loads and runs SGNet adapters

local M = {}

local function basename(path)
  local name = fs.getName(path)
  return (string.gsub(name, "%.lua$", ""))
end

local function sortedLuaFiles(dir)
  local files = {}

  if not fs.exists(dir) then return files end

  for _, name in ipairs(fs.list(dir)) do
    local path = fs.combine(dir, name)
    if not fs.isDir(path) and string.sub(name, -4) == ".lua" then
      table.insert(files, path)
    end
  end

  table.sort(files)
  return files
end

function M.create(config, network)
  local manager = {
    config = config,
    network = network,
    adapters = {},
    errors = {},
    loaded_at = nil
  }

  function manager:addError(message)
    table.insert(self.errors, tostring(message))
    if self.network and self.network.log then
      self.network.log(self.config, "ADAPTER_ERROR " .. tostring(message))
    end
  end

  function manager:load()
    self.adapters = {}
    self.errors = {}
    self.loaded_at = os.clock()

    local dir = "/sgnet/adapters"
    local files = sortedLuaFiles(dir)

    for _, path in ipairs(files) do
      local ok, adapter = pcall(dofile, path)

      if not ok then
        self:addError(path .. ": load failed: " .. tostring(adapter))
      elseif type(adapter) ~= "table" then
        self:addError(path .. ": adapter did not return a table")
      else
        local id = adapter.id or basename(path)
        adapter.id = id
        adapter.path = path

        if type(adapter.init) == "function" then
          local initOk, initErr = pcall(adapter.init, self.config, self.network)
          if not initOk then
            adapter.init_error = tostring(initErr)
            self:addError(id .. ": init failed: " .. tostring(initErr))
          end
        end

        self.adapters[id] = adapter
      end
    end

    return true
  end

  function manager:list()
    local list = {}

    for id, adapter in pairs(self.adapters) do
      table.insert(list, {
        id = id,
        name = adapter.name or id,
        path = adapter.path,
        init_error = adapter.init_error
      })
    end

    table.sort(list, function(a, b) return tostring(a.id) < tostring(b.id) end)
    return list
  end

  function manager:status()
    local list = {}

    for id, adapter in pairs(self.adapters) do
      local entry = {
        id = id,
        name = adapter.name or id,
        online = false,
        message = "",
        data = {}
      }

      if adapter.init_error then
        entry.message = "init_error: " .. tostring(adapter.init_error)
      elseif type(adapter.status) == "function" then
        local ok, result = pcall(adapter.status)
        if ok and type(result) == "table" then
          for key, value in pairs(result) do entry[key] = value end
          if entry.online == nil then entry.online = true end
        elseif ok then
          entry.online = true
          entry.message = tostring(result or "ok")
        else
          entry.message = "status_error: " .. tostring(result)
        end
      else
        entry.online = true
        entry.message = "loaded"
      end

      table.insert(list, entry)
    end

    table.sort(list, function(a, b) return tostring(a.id) < tostring(b.id) end)

    return {
      count = #list,
      adapters = list,
      errors = self.errors,
      loaded_at = self.loaded_at
    }
  end

  function manager:call(adapterId, commandName, args)
    local adapter = self.adapters[adapterId]
    if not adapter then
      return false, "Adapter not loaded: " .. tostring(adapterId)
    end

    local fn = nil

    if type(adapter.commands) == "table" then
      fn = adapter.commands[commandName]
    end

    if type(fn) ~= "function" and type(adapter[commandName]) == "function" then
      fn = adapter[commandName]
    end

    if type(fn) ~= "function" then
      return false, "Unsupported adapter command: " .. tostring(adapterId) .. "." .. tostring(commandName)
    end

    local ok, result = pcall(fn, args or {})
    if not ok then return false, tostring(result) end

    return true, result
  end

  manager:load()
  return manager
end

return M
