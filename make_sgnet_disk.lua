local AE2_BRIDGE_ADAPTER_LUA = [=====[
-- /sgnet/adapters/ae2_bridge.lua
-- Advanced Peripherals ME Bridge adapter
-- Detection intentionally checks for known ME Bridge methods instead of strict peripheral type.

local adapter = {
  id = "ae2_bridge",
  name = "AE2 ME Bridge"
}

local bridgeName = nil
local bridge = nil

local DETECTION_METHODS = {
  "getItems",
  "isOnline",
  "getStoredEnergy",
  "getUsedItemStorage",
  "getAvailableItemStorage"
}

local function typeToString(value)
  if type(value) == "table" then return table.concat(value, ",") end
  return tostring(value or "")
end

local function hasMethod(object, methodName)
  return object and type(object[methodName]) == "function"
end

local function countKnownMethods(object)
  local count = 0
  local found = {}

  for _, methodName in ipairs(DETECTION_METHODS) do
    if hasMethod(object, methodName) then
      count = count + 1
      table.insert(found, methodName)
    end
  end

  return count, found
end

local function looksLikeMEBridge(name)
  local wrapped = peripheral.wrap(name)
  if not wrapped then return false, {} end

  local methodCount, found = countKnownMethods(wrapped)
  if methodCount > 0 then return true, found end

  local kind = string.lower(typeToString(peripheral.getType(name)))
  if string.find(kind, "mebridge", 1, true) or string.find(kind, "me_bridge", 1, true) then
    return true, found
  end

  return false, found
end

local function findBridge()
  for _, name in ipairs(peripheral.getNames()) do
    local ok = looksLikeMEBridge(name)
    if ok then
      bridgeName = name
      bridge = peripheral.wrap(name)
      return bridgeName, bridge
    end
  end

  bridgeName = nil
  bridge = nil
  return nil, nil
end

local function ensureBridge()
  if bridgeName and peripheral.isPresent and peripheral.isPresent(bridgeName) then
    bridge = peripheral.wrap(bridgeName)
    if bridge then return true end
  end

  local name = findBridge()
  return name ~= nil
end

local function safeCall(methodName, ...)
  if not ensureBridge() then return false, "ME Bridge not found" end
  if not hasMethod(bridge, methodName) then return false, "Method not available: " .. tostring(methodName) end

  local args = { ... }
  local ok, result = pcall(function()
    return bridge[methodName](table.unpack(args))
  end)

  if ok then return true, result end
  return false, tostring(result)
end

local function getItems()
  local ok, result = safeCall("getItems")
  if ok and type(result) == "table" then return true, result end

  ok, result = safeCall("getItems", {})
  if ok and type(result) == "table" then return true, result end

  return false, result or "getItems failed or did not return a table"
end

local function itemCount(item)
  if type(item) ~= "table" then return 0 end
  return tonumber(item.amount or item.count or item.size or item.qty or 0) or 0
end

local function itemName(item)
  if type(item) ~= "table" then return tostring(item) end
  return tostring(item.displayName or item.display_name or item.name or item.id or item.fingerprint or "unknown")
end

local function itemTechnicalName(item)
  if type(item) ~= "table" then return tostring(item) end
  return tostring(item.name or item.id or item.fingerprint or item.displayName or "unknown")
end

local function normalizedItem(item)
  return {
    display_name = itemName(item),
    name = itemTechnicalName(item),
    count = itemCount(item)
  }
end

local function sortedItems()
  local ok, items = getItems()
  if not ok then return false, items end

  local normalized = {}
  for _, item in pairs(items) do
    table.insert(normalized, normalizedItem(item))
  end

  table.sort(normalized, function(a, b)
    if a.count == b.count then return tostring(a.display_name) < tostring(b.display_name) end
    return a.count > b.count
  end)

  return true, normalized
end

function adapter.init()
  findBridge()
end

function adapter.status()
  local found = ensureBridge()

  if not found then
    return {
      online = false,
      message = "ME Bridge not found",
      data = {}
    }
  end

  local data = {
    side = bridgeName,
    type = typeToString(peripheral.getType(bridgeName))
  }

  local okOnline, isOnline = safeCall("isOnline")
  if okOnline then data.me_online = isOnline end

  local okEnergy, energy = safeCall("getStoredEnergy")
  if okEnergy then data.stored_energy = energy end

  local okMaxEnergy, maxEnergy = safeCall("getMaxStoredEnergy")
  if okMaxEnergy then data.max_stored_energy = maxEnergy end

