local function handleResponse(senderId, message)
  local data = message.data or {}
  local command = data.command or "unknown"
  local node = nodes[tostring(senderId)]
  local nodeName = node and node.node_id or tostring(senderId)

  if data.ok then
    addAlert(nodeName .. " " .. command .. ": " .. tostring(data.message or "ok"))
  else
    addAlert(nodeName .. " " .. command .. " failed: " .. tostring(data.message or "error"))
  end

  local result = data.result
  if type(result) == "table" and type(result.items) == "table" then
    local lines = {}
    table.insert(lines, nodeName .. " " .. command .. ": " .. tostring(result.message or data.message or "results"))

    if #result.items == 0 then
      table.insert(lines, "No items returned.")
    else
      for i, item in ipairs(result.items) do
        table.insert(lines, formatItemLine(i, item))
        if i >= 9 then break end
      end
    end

    setDetails(lines)
  elseif type(result) == "table" and type(result.adapters) == "table" then
    local lines = {}
    table.insert(lines, nodeName .. " adapters:")

    for _, adapter in ipairs(result.adapters) do
      local state = adapter.online and "ONLINE" or "OFFLINE"
      table.insert(lines, "- " .. tostring(adapter.id) .. " = " .. state .. " | " .. tostring(adapter.message or ""))
    end

    if result.errors and #result.errors > 0 then
      table.insert(lines, "Errors: " .. table.concat(result.errors, " | "))
    end

    setDetails(lines)
  elseif data.message then
    setDetails({ nodeName .. " " .. command .. ": " .. tostring(data.message) })
  end
end

local function networkLoop()
