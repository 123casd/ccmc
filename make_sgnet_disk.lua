    if now - lastAnnounce > 10000 then
      announceController()
      lastAnnounce = now
    end

    local senderId, message, protocol = rednet.receive(nil, 0.5)
    if senderId and network.isValid(config, message) then
      if message.type == "command_response" then
        handleResponse(senderId, message)
      else
        updateNode(senderId, message, protocol)
      end
    end
  end
end

